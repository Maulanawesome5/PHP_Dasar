<?php
    /*
        Tipe Data pada pemrograman PHP
        String, Integer, Float, Boolean
    */
    
    // Integer --> bilangan bulat
    $data_int = 20;
    echo "Data integer = " . $data_int . "<br>";

    // Float --> bilangan desimal
    $data_float = 10.60;
    echo "Data float = " . $data_float . "<br>";

    // String --> tipe data teks
    $data_string = "Belajar pemrograman PHP";
    echo "Data string = " . $data_string . "<br>";

    // Boolean --> tipe data biner (true/false)
    $data_boolean = true;
    echo "Data boolean = " . $data_boolean . "<br>";
    $data_boolean = false;
    echo "Data boolean = " . $data_boolean . "<br>";
    
?>
